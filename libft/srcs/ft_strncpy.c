#include <string.h>

#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, int sz)
{
	char	*beg;

	beg = dst;
	while (*src && sz--)
		*dst++ = *src++;
	while (sz--)
		*dst++ = '\0';
	return (beg);
}
